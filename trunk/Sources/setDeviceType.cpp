QString MainWindow::setDeviceType( const int i_GearType )
{
    switch( i_GearType )
    {
    case _DEVICETYPEB:
        return( tr( "B" ) );
        break;
    case _DEVICETYPEC:
        return( tr( "C" ) );
        break;
    }

    return( tr( "*" ) );
}
